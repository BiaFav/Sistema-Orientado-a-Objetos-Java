
public class Seguro {
	public int tipo;
	public string tempo;
	public string cobertura;
	public int getTipo() {
		return tipo;
	}
	public void setTipo(int tipo) {
		this.tipo = tipo;
	}
	public string getTempo() {
		return tempo;
	}
	public void setTempo(string tempo) {
		this.tempo = tempo;
	}
	public string getCobertura() {
		return cobertura;
	}
	public void setCobertura(string cobertura) {
		this.cobertura = cobertura;
	}
	
	
}
